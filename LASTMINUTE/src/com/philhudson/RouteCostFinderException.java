package com.philhudson;

public class RouteCostFinderException extends Exception
{

	private static final long serialVersionUID = -5013018442807608739L;

	public RouteCostFinderException()
	{
	}

	public RouteCostFinderException(String arg0)
	{
		super(arg0);
	}

	public RouteCostFinderException(Throwable arg0)
	{
		super(arg0);
	}

	public RouteCostFinderException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public RouteCostFinderException(String arg0, Throwable arg1, boolean arg2, boolean arg3)
	{
		super(arg0, arg1, arg2, arg3);
	}

}
